/** Liefert einen Glossarbegriff (case-insensitive). */
export function getGlossaryTerm(term) {
  const t = term.toLowerCase();
  return glossary.find((g) => g.term.toLowerCase() === t || (g.abbr && g.abbr.toLowerCase() === t)) || null;
}
